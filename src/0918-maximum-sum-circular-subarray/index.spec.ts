import { maxSubarraySumCircular } from ".";

describe("0918-maximum-sum-circular-subarray", () => {
  it.each([
    [[1, -2, 3, -2], 3],
    [[5, -3, 5], 10],
    [[-3, -2, -3], -2],
    [[1, 2, 3], 6],
    [[-1, -1, 3, -1, 5], 7],
    [[-1, -1, 3, -1, 2], 4],
    [[1, 7, -1, 1, 2, 3, 4], 18],
    [[1, 7, -2, 1, 2, 3, 4, -1], 17],
    [[1, 7, -1, 1, 2, 3, 4, -1], 17],
    [[1, 7, -1, 1, 2, 3, 4, -2], 17],
    [
      [
        -9, 14, 24, -14, 12, 18, -18, -10, -10, -23, -2, -23, 11, 12, 18, -9, 9,
        -29, 12, 4, -8, 15, 11, -12, -16, -9, 19, -12, 22, 16,
      ],
      99,
    ],
  ])("%o -> %d", (arr, result) => {
    expect(maxSubarraySumCircular(arr)).toEqual(result);
  });
});
